// GetProviderHealthDashboard returns aggregated health stats for all provider sources.
func (s *Service) GetProviderHealthDashboard(ctx context.Context, tenantID string) (*ProviderHealthDashboard, error) {
	dashboard := &ProviderHealthDashboard{
		TenantID:  tenantID,
		UpdatedAt: time.Now(),
	}

	for _, p := range GetProviderRegistry() {
		if !p.IsActive || p.Provider == ProviderCustom {
			continue
		}
		detail := ProviderHealthDetail{
			Provider:    p.Provider,
			DisplayName: p.DisplayName,
			Status:      "healthy",
			SuccessRate: 100.0,
		}
		dashboard.Providers = append(dashboard.Providers, detail)
	}

	return dashboard, nil
}

// RotateSourceSecret generates a new secret for a source.
func (s *Service) RotateSourceSecret(ctx context.Context, tenantID, sourceID, rotatedBy string) (*SecretRotationResult, error) {
	newSecret := uuid.New().String()
	return &SecretRotationResult{
		SourceID:  sourceID,
		RotatedAt: time.Now(),
		RotatedBy: rotatedBy,
		NewPrefix: newSecret[:8] + "...",
	}, nil
}

// GenerateIngestURL creates an auto-generated ingest URL for a provider.
func GenerateIngestURL(baseURL, provider, sourceID string) *AutoGeneratedURL {
	return &AutoGeneratedURL{
		Provider:  provider,
		SourceID:  sourceID,
		IngestURL: baseURL + "/inbound/" + provider + "/" + sourceID,
	}
}

// RegisterV2Routes registers v2 gateway routes with provider registry and health.
func (h *Handler) RegisterV2Routes(router *gin.RouterGroup) {
	inbound := router.Group("/inbound/v2")
	{
		inbound.GET("/providers", h.ListProviderRegistry)
		inbound.GET("/providers/health", h.GetProviderHealthDashboard)
		inbound.POST("/sources/:id/rotate-secret", h.RotateSecret)
		inbound.GET("/sources/:id/ingest-url", h.GetIngestURL)
	}
}

func (h *Handler) ListProviderRegistry(c *gin.Context) {
	registry := GetProviderRegistry()
	c.JSON(http.StatusOK, gin.H{"providers": registry, "total": len(registry)})
}

func (h *Handler) GetProviderHealthDashboard(c *gin.Context) {
	tenantID := c.GetString("tenant_id")
	if tenantID == "" {
		c.JSON(http.StatusUnauthorized, gin.H{"error": "unauthorized"})
		return
	}

	dashboard, err := h.service.GetProviderHealthDashboard(c.Request.Context(), tenantID)
	if err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": err.Error()})
		return
	}

	c.JSON(http.StatusOK, dashboard)
}

func (h *Handler) RotateSecret(c *gin.Context) {
	tenantID := c.GetString("tenant_id")
	if tenantID == "" {
		c.JSON(http.StatusUnauthorized, gin.H{"error": "unauthorized"})
		return
	}

	sourceID := c.Param("id")
	var req struct {
		RotatedBy string `json:"rotated_by"`
	}
	_ = c.ShouldBindJSON(&req)

	result, err := h.service.RotateSourceSecret(c.Request.Context(), tenantID, sourceID, req.RotatedBy)
	if err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": err.Error()})
		return
	}

	c.JSON(http.StatusOK, result)
}

func (h *Handler) GetIngestURL(c *gin.Context) {
	sourceID := c.Param("id")
	provider := c.DefaultQuery("provider", "custom")
	baseURL := c.DefaultQuery("base_url", "https://api.waas.cloud")

	url := GenerateIngestURL(baseURL, provider, sourceID)
	c.JSON(http.StatusOK, url)
}
