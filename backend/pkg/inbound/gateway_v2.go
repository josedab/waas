// ProviderRegistryEntry stores metadata about a supported provider.
type ProviderRegistryEntry struct {
	Provider        string `json:"provider"`
	DisplayName     string `json:"display_name"`
	SignatureMethod string `json:"signature_method"`
	SignatureHeader string `json:"signature_header"`
	DocURL          string `json:"doc_url"`
	IsActive        bool   `json:"is_active"`
}

// ProviderHealthDashboard aggregates health stats for all providers.
type ProviderHealthDashboard struct {
	TenantID  string                  `json:"tenant_id"`
	Providers []ProviderHealthDetail  `json:"providers"`
	UpdatedAt time.Time               `json:"updated_at"`
}

// ProviderHealthDetail tracks health for a single provider.
type ProviderHealthDetail struct {
	Provider       string     `json:"provider"`
	DisplayName    string     `json:"display_name"`
	Status         string     `json:"status"` // healthy, degraded, down
	EventsTotal    int64      `json:"events_total_24h"`
	EventsFailed   int64      `json:"events_failed_24h"`
	SuccessRate    float64    `json:"success_rate"`
	AvgLatencyMs   int        `json:"avg_latency_ms"`
	LastEventAt    *time.Time `json:"last_event_at,omitempty"`
	SchemaChanges  int        `json:"schema_changes_detected"`
}

// SecretRotationResult represents the result of rotating a source secret.
type SecretRotationResult struct {
	SourceID   string    `json:"source_id"`
	Provider   string    `json:"provider"`
	RotatedAt  time.Time `json:"rotated_at"`
	RotatedBy  string    `json:"rotated_by"`
	NewPrefix  string    `json:"new_secret_prefix"`
}

// IPAllowListConfig represents IP allowlisting for a source.
type IPAllowListConfig struct {
	SourceID   string   `json:"source_id"`
	AllowedIPs []string `json:"allowed_ips"`
	IsEnabled  bool     `json:"is_enabled"`
}

// AutoGeneratedURL represents an auto-generated ingest URL for a provider.
type AutoGeneratedURL struct {
	Provider  string `json:"provider"`
	SourceID  string `json:"source_id"`
	IngestURL string `json:"ingest_url"`
}

